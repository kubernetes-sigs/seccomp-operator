package util

import (
	"github.com/pkg/errors"
	"os"
)

func WriteFileFlushed(filename string, data []byte, perm os.FileMode) error {
	f, err := os.OpenFile(filename, os.O_WRONLY|os.O_CREATE|os.O_TRUNC, perm)
	if err != nil {
		return err
	}

	_, err = f.Write(data)

	if syncErr := f.Sync(); syncErr == nil {
		err = syncErr
	}

	if err1 := f.Close(); err == nil {
		err = err1
	}

	return errors.Wrap(err, "writing flushed file")
}
